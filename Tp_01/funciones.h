#include <stdio.h>
#include <stdlib.h>

/** \brief Recibe dos numeros enteros y los suma
 *
 * \param numeroUno int
 * \param numeroDos int
 * \return Retorna un entero, el resultado de la suma de los dos numeros enteros
 *
 */
int suma (int numeroUno, int numeroDos);
/** \brief Recibe dos numeros enteros y los resta
 *
 * \param numeroUno int
 * \param numeroDos int
 * \return Retorna un entero, el resultado de la resta de los dos numeros enteros
 *
 */
int resta (int numeroUno, int numeroDos);
/** \brief Recibe dos numeros enteros y los divide, si algunos de los numeros es 0 devuelve mensaje de error
 *
 * \param numeroUno int
 * \param numeroDos int
 * \return No retorna nada
 *
 */
void division (int numeroUno, int numeroDos);
/** \brief Recibe dos numeros enteros y los multiplica
 *
 * \param numeroUno int
 * \param numeroDos int
 * \return Retorna un entero, el resultado de la multiplicacion de estos numeros
 *
 */
int multiplicacion (int numeroUno, int numeroDos);
/** \brief Recibe un entero y calcula su factorial
 *
 * \param numeroUno int
 * \return Retorna un long int, el resultado del factorial del numero
 *
 */
long int factorial(int numeroUno);

